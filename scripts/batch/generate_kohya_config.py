def main():
    parser = argparse.ArgumentParser(
        description="Generate Kohya LoRA Training Config",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
    # Generate SDXL config
    python generate_kohya_config.py \\
        --dataset-dir /path/to/10_character \\
        --output-config /path/to/config.toml \\
        --character-name luca \\
        --model-type sdxl

    # Generate with custom parameters
    python generate_kohya_config.py \\
        --dataset-dir /path/to/training_data \\
        --output-config /path/to/config.toml \\
        --character-name miguel \\
        --model-type sdxl \\
        --target-steps 3000 \\
        --vram 16
        """
    )

    parser.add_argument("--dataset-dir", required=True, type=Path,
                        help="Training dataset directory")
    parser.add_argument("--output-config", required=True, type=Path,
                        help="Output TOML config path")
    parser.add_argument("--character-name", required=True,
                        help="Character name for trigger word")
    parser.add_argument("--model-type", default="sdxl",
                        choices=["sdxl", "sd15"],
                        help="Model type (default: sdxl)")
    parser.add_argument("--output-dir", type=Path, default=None,
                        help="Training output directory (default: dataset parent)")
    parser.add_argument("--base-model", default=None,
                        help="Base model path")
    parser.add_argument("--trigger-word", default=None,
                        help="Custom trigger word (default: character name)")
    parser.add_argument("--class-token", default="person",
                        help="Class token (default: person)")
    parser.add_argument("--target-steps", type=int, default=2000,
                        help="Target training steps (default: 2000)")
    parser.add_argument("--vram", type=int, default=24,
                        help="Available VRAM in GB (default: 24)")

    args = parser.parse_args()

    # Validate dataset directory
    if not args.dataset_dir.exists():
        print(f"Error: Dataset directory not found: {args.dataset_dir}")
        return 1

    # Count images
    num_images = count_images(args.dataset_dir)
    if num_images == 0:
        print(f"Error: No images found in {args.dataset_dir}")
        return 1

    print(f"Found {num_images} training images")

    # Calculate parameters
    params = calculate_optimal_params(
        num_images=num_images,
        model_type=args.model_type,
        target_steps=args.target_steps,
        vram_gb=args.vram
    )

    print(f"\nCalculated parameters:")
    print(f"  Repeats:       {params.repeats}")
    print(f"  Epochs:        {params.num_epochs}")
    print(f"  Batch size:    {params.batch_size}")
    print(f"  Total steps:   {params.total_steps}")
    print(f"  Learning rate: {params.learning_rate}")
    print(f"  Network dim:   {params.network_dim}")
    print(f"  Network alpha: {params.network_alpha}")

    # Output directory
    output_dir = args.output_dir or args.dataset_dir.parent / "output"

    # Generate config
    if args.model_type == "sdxl":
        base_model = args.base_model or "/mnt/c/ai_models/checkpoints/waiANINSFWPONYXL_v90.safetensors"
        config = generate_sdxl_config(
            dataset_dir=args.dataset_dir,
            output_dir=output_dir,
            character_name=args.character_name,
            params=params,
            base_model=base_model,
            trigger_word=args.trigger_word,
            class_token=args.class_token
        )
    else:
        base_model = args.base_model or "runwayml/stable-diffusion-v1-5"
        config = generate_sd15_config(
            dataset_dir=args.dataset_dir,
            output_dir=output_dir,
            character_name=args.character_name,
            params=params,
            base_model=base_model,
            trigger_word=args.trigger_word,
            class_token=args.class_token
        )

    # Save config
    args.output_config.parent.mkdir(parents=True, exist_ok=True)
    with open(args.output_config, "w", encoding="utf-8") as f:
        f.write(config)

    print(f"\n✓ Config saved to: {args.output_config}")

    # Save summary
    summary = {
        "dataset_dir": str(args.dataset_dir),
        "num_images": num_images,
        "model_type": args.model_type,
        "character_name": args.character_name,
        "params": {
            "repeats": params.repeats,
            "epochs": params.num_epochs,
            "batch_size": params.batch_size,
            "total_steps": params.total_steps,
            "learning_rate": params.learning_rate,
            "network_dim": params.network_dim,
            "network_alpha": params.network_alpha
        }
    }

    summary_path = args.output_config.with_suffix(".json")
    with open(summary_path, "w", encoding="utf-8") as f:
        json.dump(summary, f, indent=2)

    print(f"✓ Summary saved to: {summary_path}")

    return 0


if __name__ == "__main__":
    exit(main())
