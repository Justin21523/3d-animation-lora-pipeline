@dataclass
class TrainingParams:
    """Training parameters based on dataset analysis"""
    num_images: int
    total_steps: int
    num_epochs: int
    repeats: int
    batch_size: int
    learning_rate: float
    network_dim: int
    network_alpha: int


def count_images(dataset_dir: Path) -> int:
    """Count training images in dataset directory"""
    extensions = {".jpg", ".jpeg", ".png", ".webp"}
    count = 0

    for path in dataset_dir.rglob("*"):
        if path.suffix.lower() in extensions:
            count += 1

    return count


def calculate_optimal_params(
    num_images: int,
    model_type: str = "sdxl",
    target_steps: int = 2000,
    vram_gb: int = 24
) -> TrainingParams:
    """
    Calculate optimal training parameters based on dataset size.

    Args:
        num_images: Number of training images
        model_type: "sdxl" or "sd15"
        target_steps: Target total training steps
        vram_gb: Available VRAM in GB

    Returns:
        TrainingParams with calculated values
    """
    # Batch size based on VRAM
    if model_type == "sdxl":
        if vram_gb >= 24:
            batch_size = 2
        elif vram_gb >= 16:
            batch_size = 1
        else:
            batch_size = 1
    else:  # SD 1.5
        if vram_gb >= 16:
            batch_size = 4
        elif vram_gb >= 8:
            batch_size = 2
        else:
            batch_size = 1

    # Calculate repeats and epochs
    # Formula: total_steps = num_images * repeats * epochs / batch_size
    # Start with reasonable epochs
    if num_images < 50:
        num_epochs = 20
    elif num_images < 100:
        num_epochs = 15
    elif num_images < 200:
        num_epochs = 10
    elif num_images < 500:
        num_epochs = 8
    else:
        num_epochs = 6

    # Calculate repeats needed to hit target steps
    # steps_per_epoch = num_images * repeats / batch_size
    # total_steps = steps_per_epoch * num_epochs
    # repeats = (target_steps * batch_size) / (num_images * num_epochs)
    repeats = max(1, round((target_steps * batch_size) / (num_images * num_epochs)))

    # Recalculate actual total steps
    steps_per_epoch = (num_images * repeats) // batch_size
    total_steps = steps_per_epoch * num_epochs

    # Learning rate based on model type
    if model_type == "sdxl":
        learning_rate = 1e-4
        network_dim = 32
        network_alpha = 16
    else:
        learning_rate = 1e-4
        network_dim = 128
        network_alpha = 64

    return TrainingParams(
        num_images=num_images,
        total_steps=total_steps,
        num_epochs=num_epochs,
        repeats=repeats,
        batch_size=batch_size,
        learning_rate=learning_rate,
        network_dim=network_dim,
        network_alpha=network_alpha
    )


